package dto;

public class CarDTO {
	
	private int num;
	private String carName;
	private int enginPrice;
	private int tirePrice;
	private String optName;
	private int optPrice;
	private int totalPrice;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getCarName() {
		return carName;
	}
	public void setCarName(String carName) {
		this.carName = carName;
	}
	public int getEnginPrice() {
		return enginPrice;
	}
	public void setEnginPrice(int enginPrice) {
		this.enginPrice = enginPrice;
	}
	public int getTirePrice() {
		return tirePrice;
	}
	public void setTirePrice(int tirePrice) {
		this.tirePrice = tirePrice;
	}
	public String getOptName() {
		return optName;
	}
	public void setOptName(String optName) {
		this.optName = optName;
	}
	public int getOptPrice() {
		return optPrice;
	}
	public void setOptPrice(int optPrice) {
		this.optPrice = optPrice;
	}
	public int getTotalPrice() {
		return totalPrice;
	}
	public void setTotalPrice(int totalPrice) {
		this.totalPrice = totalPrice;
	}
	
}// class END
